package org.twozo.components.driver;

/**
 * Enum representing different types of web browsers.
 */
enum BrowserType {

    /**
     * Google Chrome browser.
     */
    CHROME,

    /**
     * Mozilla Firefox browser.
     */
    FIREFOX,

    /**
     * Apple Safari browser.
     */
    SAFARI,

    /**
     * Internet Explorer browser.
     */
    INTERNET_EXPLORER,

    /**
     * Microsoft Edge browser.
     */
    EDGE;
}
