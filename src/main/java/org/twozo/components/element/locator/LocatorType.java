package org.twozo.components.element.locator;

/**
 * Enum representing different types of locators for web elements.
 */
public enum LocatorType {

    /**
     * Locator by ID
     */
    ID,

    /**
     * Locator by NAME
     */
    NAME,

    /**
     * Locator by CLASS_NAME
     */
    CLASS_NAME,

    /**
     * Locator by XPATH
     */
    XPATH,

    /**
     * Locator by CSS_SELECTOR
     */
    CSS_SELECTOR,

    /**
     * Locator by LINKED_TEXT
     */
    LINKED_TEXT,

    /**
     * Locator by PARTIALLY_LINKED_TEXT
     */
    PARTIALLY_LINKED_TEXT,

    /**
     * Locator by TAG_NAME
     */
    TAG_NAME;

}

